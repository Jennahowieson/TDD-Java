public class Calculator {

    public static int add(int int1, int int2){
        return int1 + int2;
    }

    public static int min(int int1, int int2){
        return int1 - int2;
    }

    public static int multiply(int int1, int int2){
        return int1 * int2;
    }

    public static double divide(int int1, int int2){
        return int1 / int2;
    }
}
